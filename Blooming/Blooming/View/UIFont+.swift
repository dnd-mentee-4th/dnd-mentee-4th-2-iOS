import UIKit

extension UIFont {
    static func spoqaBold(_ size: CGFloat) -> UIFont {
        return UIFont(name: "SpoqaHanSansNeo-Bold", size: size)!
    }
    
    static func spoqaMedium(_ size: CGFloat) -> UIFont {
        return UIFont(name: "SpoqaHanSansNeo-Medium", size: size)!
    }
    
    static func spoqaRegular(_ size: CGFloat) -> UIFont {
        return UIFont(name: "SpoqaHanSansNeo-Regular", size: size)!
    }
}
